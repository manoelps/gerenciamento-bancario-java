    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        
        // Recebo os valores digitados no formulario
        contaCorrente = jTextFieldContaCorrente.getText();
        contaPoupanca = jTextFieldContaPoupanca.getText();
        
        //declaro o atributo para monitor erros do formulario
        boolean erros = false;
        
        //executo uma valida√ß√£o simples se o formulario est√° indo em branco j√° removendo os espa√ßos em branco
        if(contaCorrente.trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Informe a sua conta corrente");
            erros = true;
        }else if(contaPoupanca.trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Informe a sua conta poupan√ßa");
            erros = true;
        }
        
        if(!erros){
            
            // atribui os n√∫meros das contas nas variaveis globais correspondentes a conta corrente e poupan√ßa
            // para que sejam acessadas em outras janelas
            GerenciamentoContaBancaria.contaCorrenteUsuario = Integer.parseInt(contaCorrente);
            GerenciamentoContaBancaria.contaPoupancaUsuario = Integer.parseInt(contaPoupanca);
                    
            // Criar uma inst√¢ncia do formul√°rio Main (formulario principal)
            FormMain formularioMain = new FormMain();

            //Centraliza o formul√°rio no centro da tela
            formularioMain.setLocationRelativeTo(null);
        
            // Configurar o comportamento padr√£o quando o formul√°rio √© fechado
            formularioMain.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            
            //Oculta o formul√°rio atual
            setVisible(false);

            // Tornar o formul√°rio Main (formulario principal) vis√≠vel
            formularioMain.setVisible(true);      
        
        }
    }//GEN-LAST:event_jButton8ActionPerformed

